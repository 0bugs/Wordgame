GamoogaClient.prototype.send = function(data) {
    var self = this;
    
    var msg = {};
    if (self.isRoomSess == 1) {
        msg['type'] = 2;
    } else {
        msg['type'] = 502;
    }
    msg['data'] = data;
    self._send(msg);
};

GamoogaClient.prototype.close = function() {
    var self = this;
    
    self.self_close = true;
    self.sock.close();
};

GamoogaClient.prototype._send = function(json) {
    var self = this;
    
    str = JSON.stringify(json);
    len = new String(str.length.toString(16));
    len = len.leftPad(8);
    self.sock.write(len+str);
};

GamoogaClient._makeXHR = function(method, url) {
    var xhr = new XMLHttpRequest();
    if ("withCredentials" in xhr){
        xhr.open(method, url, true);
    } else if (typeof XDomainRequest != "undefined"){
        xhr = new XDomainRequest();
        xhr.open(method, url);
    } else {
        xhr = null;
    }
    return xhr;
};

